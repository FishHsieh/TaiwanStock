        [string]$Text































    )































































    $encoded = [System.Net.WebUtility]::HtmlEncode($Text)































    $encoded = [regex]::Replace($encoded, '\*\*(.+?)\*\*', '<strong>$1</strong>')































    $encoded = [regex]::Replace($encoded, '(https?://[^\s<]+)', '<a href="$1" target="_blank" rel="noopener noreferrer">$1</a>')































    return $encoded































}































































function Test-ReportMarkdownTableSeparatorLine {































    param(































        [Parameter(Mandatory = $true)]































        [string]$Line































    )































































    $trimmed = $Line.Trim()































    if ($trimmed -notmatch '^\|.*\|$') {































        return $false































    }































































    $cells = $trimmed.Trim('|').Split('|')































    if ($cells.Count -eq 0) {































        return $false































    }































































    foreach ($cell in $cells) {































        $token = $cell.Trim()































        if ($token -notmatch '^:?-{3,}:?$') {































            return $false































        }































    }































































    return $true































}































































function Get-ReportMarkdownTableAlignment {































