function Remove-ReportTableRowsWithoutMovingAverage {































    param(































        [Parameter(Mandatory = $true)]































        [string]$Text































    )































































    $normalizedText = $Text -replace "`r`n", "`n"































    $lines = $normalizedText -split "`n"































    $sb = [System.Text.StringBuilder]::new()































    $tableLines = [System.Collections.Generic.List[string]]::new()































































    $flushTable = {































        if ($tableLines.Count -eq 0) {































            return































        }































































        if ($tableLines.Count -ge 2 -and (Test-ReportMarkdownTableSeparatorLine -Line $tableLines[1].Trim())) {































            $tableOutput = [System.Collections.Generic.List[string]]::new()































            [void]$tableOutput.Add($tableLines[0])































            [void]$tableOutput.Add($tableLines[1])































































            for ($i = 2; $i -lt $tableLines.Count; $i++) {































                $rowLine = $tableLines[$i]































                $trimmedRow = $rowLine.Trim()































                if ([string]::IsNullOrWhiteSpace($trimmedRow)) {































                    continue































                }































































                $cells = @($trimmedRow.Trim('|').Split('|') | ForEach-Object { $_.Trim() })































                if (Test-ReportMarkdownTableRowHasComparableMovingAverage -Cells $cells) {































                    [void]$tableOutput.Add($rowLine)































                }































            }































































            if ($tableOutput.Count -gt 2) {































                foreach ($outputLine in $tableOutput) {































                    [void]$sb.AppendLine($outputLine)































                }































            }































        }































        else {































            foreach ($tableLine in $tableLines) {































                [void]$sb.AppendLine($tableLine)































            }































        }































































        $tableLines.Clear()































    }































































    foreach ($line in $lines) {































        $trimmedLine = $line.TrimStart()































        if ($trimmedLine.StartsWith('|')) {































            [void]$tableLines.Add($line)































            continue































        }































































        & $flushTable































        [void]$sb.AppendLine($line)































    }































































    & $flushTable































    return $sb.ToString().TrimEnd()































}































function Convert-MarkdownTableBlockToHtml {































    param(































        [Parameter(Mandatory = $true)]































        [string[]]$Lines































    )































































    $rows = New-Object 'System.Collections.Generic.List[object]'































    $alignments = @()































































    foreach ($line in $Lines) {































        $trimmed = $line.Trim()































        if ([string]::IsNullOrWhiteSpace($trimmed)) {































            continue































        }































        if (Test-ReportMarkdownTableSeparatorLine -Line $trimmed) {































            $segments = $trimmed.Trim('|').Split('|')































            $alignments = @($segments | ForEach-Object { Get-ReportMarkdownTableAlignment -Cell $_ })































            continue































        }































        if ($trimmed -notmatch '^\|.*\|$') {































            continue































        }































































        $cells = $trimmed.Trim('|').Split('|')































        $row = @()































        foreach ($cell in $cells) {































            $row += ,([string]$cell).Trim()































        }































        [void]$rows.Add($row)































    }































































    if ($rows.Count -lt 2) {































        return ''































    }































































    $columnCount = [Math]::Max(































        ($rows | ForEach-Object { $_.Count } | Measure-Object -Maximum).Maximum,































        $alignments.Count































    )































































    if ($columnCount -le 0) {































        return ''































    }































































    $volumeColumnIndexes = [System.Collections.Generic.HashSet[int]]::new()

    $isClassificationTable = $false

    if ($rows.Count -gt 0 -and $rows[0].Count -ge 2) {

        $isClassificationTable = ([string]$rows[0][0] -match '^(?:\u5206\u985e|\u985e\u5225)$' -and [string]$rows[0][1] -match '^\u6a19\u7684$')

    }

    $targetColumnIndex = Get-ReportTableHeaderIndex -Headers $rows[0] -Patterns @('^(?:標的|名稱|市場)$')

    if ($targetColumnIndex -lt 0) {

        $targetColumnIndex = if ($isClassificationTable) { 1 } else { 0 }

    }

    $recommendationColumnIndex = Get-ReportTableHeaderIndex -Headers $rows[0] -Patterns @('^建議$', '^操作建議$', '^結論$')

    $sb = [System.Text.StringBuilder]::new()














    [void]$sb.AppendLine('<div class="table-wrap">')































    [void]$sb.AppendLine('  <table class="report-table">')































    [void]$sb.AppendLine('    <thead>')































    [void]$sb.AppendLine('      <tr>')































    for ($i = 0; $i -lt $columnCount; $i++) {































        $headerText = if ($i -lt $rows[0].Count) { [string]$rows[0][$i] } else { '' }































        $alignment = if ($i -lt $alignments.Count -and $alignments[$i]) { [string]$alignments[$i] } else { 'left' }































        if ($headerText -match '^(?:量能|成交量分析)$') { [void]$volumeColumnIndexes.Add($i) }



        $encodedHeader = Convert-ReportInlineText -Text $headerText































        [void]$sb.AppendLine(('        <th style="text-align:{0}">{1}</th>' -f $alignment, $encodedHeader))































    }































    [void]$sb.AppendLine('      </tr>')































    [void]$sb.AppendLine('    </thead>')































    [void]$sb.AppendLine('    <tbody>')































    for ($rowIndex = 1; $rowIndex -lt $rows.Count; $rowIndex++) {































        $row = $rows[$rowIndex]































        if (-not (Test-ReportMarkdownTableRowHasComparableMovingAverage -Cells $row)) {































            continue































        }































































        [void]$sb.AppendLine('      <tr>')































        $rowRecommendationClass = ''

        if ($recommendationColumnIndex -ge 0 -and $recommendationColumnIndex -lt $row.Count) {

            $rowRecommendationClass = Get-ReportRecommendationCellClass -Recommendation ([string]$row[$recommendationColumnIndex])

        }
        elseif ($row.Count -gt 0) {

            $rowRecommendationClass = Get-ReportRecommendationCellClass -Recommendation ([string]$row[-1])

        }



























































        for ($i = 0; $i -lt $columnCount; $i++) {































            $cellText = if ($i -lt $row.Count) { [string]$row[$i] } else { '' }































            $alignment = if ($i -lt $alignments.Count -and $alignments[$i]) { [string]$alignments[$i] } else { 'left' }































            $encodedCell = Convert-ReportInlineText -Text $cellText































            $cellClasses = [System.Collections.Generic.List[string]]::new()







            if ($i -eq $targetColumnIndex -and -not [string]::IsNullOrWhiteSpace($rowRecommendationClass)) {



                [void]$cellClasses.Add($rowRecommendationClass)



            }







            if ($volumeColumnIndexes.Contains($i)) {



                $volumeCellClass = Get-ReportVolumeAnalysisCellClass -CellText $cellText



                if ($volumeCellClass) { [void]$cellClasses.Add($volumeCellClass) }



            }







